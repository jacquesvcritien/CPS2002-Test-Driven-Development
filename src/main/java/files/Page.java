package files;

/**
 * This is the class used to represent a page
 */
public class Page {

    //this contains the html of this page
    private String HTML;

    /**
     * Method to return html
     * @return returns html
     */
    public String getHTML()
    {
        return HTML;
    }

    /**
     * HTML setter
     * @param HTML html to set
     */
    public void setHTML(String HTML) {
        this.HTML = HTML;
    }
}
